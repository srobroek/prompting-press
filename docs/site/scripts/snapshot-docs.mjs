/**
 * snapshot-docs.mjs — docs:snapshot task body (spec 012, T012)
 *
 * Usage: node scripts/snapshot-docs.mjs --version X.Y.Z
 *   (or via moon: moon run docs:snapshot -- --version X.Y.Z)
 *
 * Steps:
 *   (a) Spec-011 adapter: regenerate per-version API refs via gen-api-refs.mjs
 *       --version X.Y --out <staging>. Falls back to copying the current
 *       reference/*.mdx as-is + logs a "pending spec-011" warning (IO-2).
 *   (b) Freeze current docs (src/content/docs/** + staged API refs) into
 *       src/versions/vX.Y/ — create if new minor, overwrite if patch rollup.
 *   (c) Update src/data/versions.json via version.mjs canonical writer.
 *
 * Idempotent: deterministic copy + canonical JSON → twice-run zero git diff (SC-004).
 * Stable Node APIs only (fs, path, child_process) — runs under node >=22.12.0 (IO-3).
 */

import {
  existsSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  cpSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { resolve, dirname, relative, join, extname, basename } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

import {
  parse,
  bucket,
  bucketAction,
  readManifest,
  writeManifest,
} from "./lib/version.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SITE_ROOT = resolve(__dirname, "..");
const SRC_DOCS = resolve(SITE_ROOT, "src/content/docs");
const SRC_VERSIONS = resolve(SITE_ROOT, "src/versions");
const MANIFEST_PATH = resolve(SITE_ROOT, "src/data/versions.json");
const GEN_API_REFS = resolve(__dirname, "gen-api-refs.mjs");
// Repo root = three levels up from docs/site/scripts (scripts → site → docs → root).
const REPO_ROOT = resolve(SITE_ROOT, "../..");
const ROOT_CHANGELOG = resolve(REPO_ROOT, "CHANGELOG.md");

// ---------------------------------------------------------------------------
// Argument parsing
// ---------------------------------------------------------------------------

function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--version" && argv[i + 1]) {
      args.version = argv[i + 1];
      i++;
    }
  }
  return args;
}

const args = parseArgs(process.argv.slice(2));

if (!args.version) {
  console.error(
    "[snapshot-docs] ERROR: --version X.Y.Z is required.\n" +
      "  Usage: moon run docs:snapshot -- --version 1.2.3",
  );
  process.exit(1);
}

// Validate the version string immediately (FR-013: loud fail on bad version).
let parsed;
try {
  parsed = parse(args.version);
} catch (e) {
  console.error(`[snapshot-docs] ERROR: ${e.message}`);
  process.exit(1);
}

const version = args.version; // "X.Y.Z"
const minorBucket = bucket(version); // "X.Y"
const versionDir = resolve(SRC_VERSIONS, `v${minorBucket}`); // src/versions/vX.Y

console.log(`[snapshot-docs] Snapshotting version ${version} → bucket v${minorBucket}`);

// ---------------------------------------------------------------------------
// Step (a): Spec-011 adapter — per-version API refs
// ---------------------------------------------------------------------------

// Staging dir for API refs generated by spec-011's gen-api-refs.mjs
const stagingDir = resolve(SITE_ROOT, `.snapshot-staging-${minorBucket}`);

let apiRefsStagedDir = null; // will be set if we got staged API refs

if (existsSync(GEN_API_REFS)) {
  // spec-011 is present: call it for per-version API refs
  console.log(`[snapshot-docs] Running gen-api-refs.mjs --version ${minorBucket} --out ${stagingDir}`);
  mkdirSync(stagingDir, { recursive: true });
  const result = spawnSync(
    process.execPath,
    [GEN_API_REFS, "--version", minorBucket, "--out", stagingDir],
    { stdio: "inherit", cwd: SITE_ROOT },
  );
  if (result.status !== 0) {
    console.error(`[snapshot-docs] ERROR: gen-api-refs.mjs exited with status ${result.status}`);
    process.exit(1);
  }
  apiRefsStagedDir = stagingDir;
} else {
  // spec-011 not yet merged — fallback (IO-2): copy current reference/*.mdx as-is
  console.warn(
    "[snapshot-docs] WARNING: scripts/gen-api-refs.mjs not found. " +
      "Per-version API ref regeneration is pending spec-011. " +
      "Copying current reference pages as-is.",
  );
  // No staged dir — the current reference files are already in src/content/docs/reference/
  // and will be copied in step (b) as part of the full docs copy.
}

// ---------------------------------------------------------------------------
// Step (b): Freeze current docs into src/versions/vX.Y/
// ---------------------------------------------------------------------------

// Determine bucket action vs. current manifest state.
const manifest = readManifest(MANIFEST_PATH);
const existingEntry = manifest.versions.find((v) => v.minor === minorBucket);
const isNewBucket = !existingEntry;

if (isNewBucket) {
  console.log(`[snapshot-docs] Creating new bucket: src/versions/v${minorBucket}/`);
} else {
  console.log(
    `[snapshot-docs] Overwriting existing bucket (patch rollup): src/versions/v${minorBucket}/ ` +
      `(was ${existingEntry.lastPatch} → ${version})`,
  );
}

// Ensure versionDir exists (create or reuse).
mkdirSync(versionDir, { recursive: true });

// Wipe and repopulate for deterministic output (idempotence).
// Only wipe .mdx/.md files and subdirs — preserves any other files that may be there.
// For simplicity: wipe everything then re-copy. This is safe because it's snapshot-owned.
for (const entry of readdirSync(versionDir)) {
  const fullPath = join(versionDir, entry);
  rmSync(fullPath, { recursive: true, force: true });
}

// Copy src/content/docs/** into src/versions/vX.Y/
cpSync(SRC_DOCS, versionDir, { recursive: true });

// Overlay staged API refs if present (replaces the reference/ subdirectory).
if (apiRefsStagedDir && existsSync(apiRefsStagedDir)) {
  const stagedRefDir = resolve(apiRefsStagedDir, "reference");
  if (existsSync(stagedRefDir)) {
    const targetRefDir = resolve(versionDir, "reference");
    mkdirSync(targetRefDir, { recursive: true });
    cpSync(stagedRefDir, targetRefDir, { recursive: true });
    console.log(`[snapshot-docs] Overlaid staged API refs from ${stagedRefDir}`);
  }
  // Clean up staging dir
  rmSync(stagingDir, { recursive: true, force: true });
}

console.log(`[snapshot-docs] Frozen docs to ${versionDir}`);

// ---------------------------------------------------------------------------
// Step (b2): Capture this version's changelog into the frozen tree.
//
// release-please maintains a root CHANGELOG.md (per-version sections headed by
// `## [X.Y.Z]` or `## X.Y.Z`). Extract the section for THIS version and write it
// as changelog.mdx inside the frozen tree, so each /v/X.Y/ has its own changelog
// page. Pre-release (no CHANGELOG.md yet), this is a no-op with a logged note —
// the snapshot still succeeds (R5; degrades gracefully like the spec-011 adapter).
// ---------------------------------------------------------------------------

/**
 * Extract the changelog section for a specific version from a Keep-a-Changelog /
 * release-please CHANGELOG body. Matches a heading line containing the exact
 * version (e.g. `## [1.2.0]`, `## 1.2.0`, `## [1.2.0](url) (date)`) and returns
 * everything up to the next `## ` heading. Returns null if not found.
 */
function extractChangelogSection(changelogText, ver) {
  const lines = changelogText.split(/\r?\n/);
  // A version heading: starts with "## ", and the version token appears as a
  // whole token (bracketed or bare) — avoids matching 1.2.0 inside 11.2.0.
  const verEsc = ver.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const headingRe = new RegExp(`^##\\s+\\[?${verEsc}\\b`);
  const anyHeadingRe = /^##\s+/;
  let start = -1;
  for (let i = 0; i < lines.length; i++) {
    if (headingRe.test(lines[i])) {
      start = i;
      break;
    }
  }
  if (start === -1) return null;
  let end = lines.length;
  for (let i = start + 1; i < lines.length; i++) {
    if (anyHeadingRe.test(lines[i])) {
      end = i;
      break;
    }
  }
  return lines.slice(start, end).join("\n").trim();
}

{
  const changelogOut = resolve(versionDir, "changelog.mdx");
  let section = null;
  if (existsSync(ROOT_CHANGELOG)) {
    section = extractChangelogSection(readFileSync(ROOT_CHANGELOG, "utf-8"), version);
  }
  if (section) {
    const frontmatter = `---\ntitle: "Changelog — ${version}"\ndescription: "What changed in Prompting Press ${version}."\n---\n\n`;
    writeFileSync(changelogOut, frontmatter + section + "\n", "utf-8");
    console.log(`[snapshot-docs] Captured changelog for ${version} → ${changelogOut}`);
  } else {
    // Graceful pre-release fallback: no root CHANGELOG.md, or no matching
    // section yet. Write a stub so the page exists and the slug is stable.
    const stub =
      `---\ntitle: "Changelog — ${version}"\ndescription: "What changed in Prompting Press ${version}."\n---\n\n` +
      `_No changelog entry was found for ${version} at snapshot time._\n`;
    writeFileSync(changelogOut, stub, "utf-8");
    console.warn(
      `[snapshot-docs] WARNING: no CHANGELOG.md section for ${version} ` +
        `(${existsSync(ROOT_CHANGELOG) ? "section not found" : "no root CHANGELOG.md"}). Wrote a stub changelog page.`,
    );
  }
}

// ---------------------------------------------------------------------------
// Step (c): Update src/data/versions.json
// ---------------------------------------------------------------------------

/**
 * Collect all page slugs present in the versioned tree.
 * Slug = path relative to versionDir, without extension, with index.mdx → "".
 */
function collectSlugs(dir, base = dir) {
  const slugs = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      slugs.push(...collectSlugs(fullPath, base));
    } else if ([".md", ".mdx"].includes(extname(entry.name))) {
      const rel = relative(base, fullPath)
        .replace(/\\/g, "/")
        .replace(/\.(mdx?|md)$/, "");
      slugs.push(rel === "index" ? "" : rel);
    }
  }
  return slugs.sort();
}

const slugs = collectSlugs(versionDir);

// Compute the new highest minor (for isLatest determination).
// We compare numeric major.minor pairs, ignoring `next`.
function parseMinor(m) {
  const parts = m.split(".").map(Number);
  return parts.length >= 2 && !isNaN(parts[0]) && !isNaN(parts[1])
    ? { major: parts[0], minor: parts[1] }
    : null;
}

function isHigher(a, b) {
  // a > b?
  const pa = parseMinor(a);
  const pb = parseMinor(b);
  if (!pa) return false;
  if (!pb) return true;
  if (pa.major !== pb.major) return pa.major > pb.major;
  return pa.minor > pb.minor;
}

// Build the updated versions array.
const existingReleased = manifest.versions.filter(
  (v) => v.minor !== "next" && v.minor !== minorBucket,
);

// Build the entry for this snapshot.
const thisEntry = {
  isLatest: false, // will be set below
  label: minorBucket,
  lastPatch: version,
  minor: minorBucket,
  slugs,
};

// Keep the `next` entry if it existed (pre-1.0 seed or ongoing main).
const nextEntry = manifest.versions.find((v) => v.minor === "next");

// Combine all released entries (existing + this one, deduped).
let releasedEntries = [...existingReleased, thisEntry];

// Determine the highest released minor.
let highestMinor = minorBucket;
for (const e of releasedEntries) {
  if (isHigher(e.minor, highestMinor)) {
    highestMinor = e.minor;
  }
}

// Sort newest-first (semver descending, ignoring next).
releasedEntries.sort((a, b) => (isHigher(a.minor, b.minor) ? -1 : 1));

// Set isLatest + label.
for (const e of releasedEntries) {
  e.isLatest = e.minor === highestMinor;
  e.label = e.isLatest ? `${e.minor} (latest)` : e.minor;
}

// Build final versions array: released entries first (newest → oldest), then `next`.
// The `next` entry's isLatest stays false once real releases exist.
const versions = nextEntry
  ? [
      ...releasedEntries,
      { ...nextEntry, isLatest: false, label: "next (main)" },
    ]
  : releasedEntries;

const newManifest = {
  latest: highestMinor,
  versions,
};

writeManifest(newManifest, MANIFEST_PATH);
console.log(`[snapshot-docs] Updated versions.json: latest=${highestMinor}, bucket=${minorBucket}, lastPatch=${version}`);
console.log(`[snapshot-docs] Versions: ${versions.map((v) => v.minor + (v.isLatest ? "*" : "")).join(", ")}`);
console.log(`[snapshot-docs] Done.`);
